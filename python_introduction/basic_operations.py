#To practice basic arithmetic operations in Python by performing predefined calculations
number_1 = 10
number_2 = 5
# Addition
sum = number_1 + number_2
print(f"Addition of {number_1} and {number_2} is: {sum}")
# Subtraction
difference = number_1 - number_2
print(f"Subtraction of {number_1} and {number_2} is: {difference}")
# Multiplication
product = number_1 * number_2
print(f"Multiplication of {number_1} and {number_2} is: {product}")
